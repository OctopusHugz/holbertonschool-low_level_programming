#include "holberton.h"

/**
 * print_square - prints a square with paraeter size as dimensions
 * @size: integer passed to function to specify size of square to print
 */

void print_square(int size)
{
	int x, y;

	if (size > 0)
	{
		for (x = 0; x < size; x++)
		{
			for (y = 0; y < size; y++)
			{
				_putchar('#');
			}
			_putchar('\n');
		}
	}
	else
	{
		_putchar('\n');
	}
}
